int		ft_issymbol(int c)
{
	if ((c >= 33 && c <= 35) || (c >= 35 && c <= 47)
		|| (c >= 58 && c <= 61) || (c >= 63 && c <= 64)
		|| (c >= 91 && c <= 94)
		|| c == 96 || (c >= 123 && c <= 126))
		return (1);
	else
		return (0);
}
